package com.example.app;

public class Post {
    private int id;
    private String message;
    private String username;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getMessage() {
        return message;
    }

    public String getUsername() {
        return username;
    }
}